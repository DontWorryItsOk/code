#include <stdio.h>
#include <time.h>
#include <stdlib.h>

int main() {
int N, i, j, max;
int mas[N][N] = {{1, 2, 3}, {4, 5, 6}, {7, 8, 9}};
int max = mas[0][1]; // предполагаем, что первый элемент выше побочной диагонали - максимальный

// Вывод исходной матрицы
printf("Исходная матрица:\n");
for (int i = 0; i < N; i++) {
for (int j = 0; j < N; j++) {
printf("%d\t", mas[i][j]);
}
printf("\n");
}

// Вывод части матрицы выше побочной диагонали в треугольном виде
printf("\nЧасть матрицы выше побочной диагонали в треугольном виде:\n");
for (int i = 0; i < N; i++) {
for (int j = 0; j < N; j++) {
if (j > i) {
printf("%d\t", mas[i][j]);

// Поиск максимального элемента выше побочной диагонали
if (mas[i][j] > max) {
max = mas[i][j];
}
} else {
printf("\t");
}
}
printf("\n");
}

// Вывод максимального элемента
printf("\nМаксимальный элемент выше побочной диагонали: %d\n", max);

return 0;
}
